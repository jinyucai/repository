import java.util.List;

@RestController
@RequestMapping("/user")
public class UserController {

    @Autowired
    private UserService userService;

    @RequestMapping("/userAll")
    public List<UmsMember> getAllUsers(){
        return userService.getUmsMenber();
    }

}
